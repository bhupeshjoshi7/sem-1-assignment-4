marks = int(float(input("Enter your marks :")))

if marks <= 25:
    print("Your grade is F")
if 25< marks <= 45:
    print("Your grade is E")
if 45<marks <= 50:
    print("Your grade is D")
if 50<marks <= 60:
    print("Your grade is C")
if 60<marks <= 80:
    print("Your grade is B")
if marks>80:
    print("Your grade is A")    